package productos;

/**
 * Enumeración para guardar materiales de fabricación.
 * @author Bruno Montero
 * @version 1.0
 */
public enum Material 
{
    /**
     * Plástico ABS
     */
    ABS,

    /**
     * Aluminio, acero u otras cariantes
     */
    METAL,

    /**
     * Plástico PVC
     */
    PVC,

    /**
     * Reconocido material para figuras.
     */
    RESINA,

    /**
     * Textiles basados en hilos de diferentes materiales
     */
    TELA,

    /**
     * Plásticos vinilados.
     */
    VINILO,
    
}
